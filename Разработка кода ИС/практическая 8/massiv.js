// Задание 3
var arr3 = []; // Создаем новый массив
for (var i = 0; i < n; i++) {
    // Генерируем 15 случайных чисел в интервале [-10, 30]
    arr3.push(Math.floor(Math.random() * (30 - (-10) + 1)) + (-10));
}
console.log("Массив:", arr3); // Выводим массив

// Переменные для сумм и счетчиков
var positiveSum3 = 0, negativeSum3 = 0;
var positiveCount3 = 0, negativeCount3 = 0, zeroCount = 0;

for (var j = 0; j < arr3.length; j++) {
    if (arr3[j] > 0) { // Проверяем, является ли элемент положительным
        positiveSum3 += arr3[j]; // Добавляем к сумме положительных
        positiveCount3++; // Увеличиваем счетчик положительных
    } else if (arr3[j] < 0) { // Проверяем, является ли элемент отрицательным
        negativeSum3 += arr3[j]; // Добавляем к сумме отрицательных
        negativeCount3++; // Увеличиваем счетчик отрицательных
    } else {
        zeroCount++; // Увеличиваем счетчик нулей
    }
}

// Вычисляем средние значения
var averagePositive3 = positiveCount3 > 0 ? positiveSum3 / positiveCount3 : 0;
var averageNegative3 = negativeCount3 > 0 ? negativeSum3 / negativeCount3 : 0;

// Выводим результаты
console.log("Среднее арифметическое положительных чисел:", averagePositive3);
console.log("Среднее арифметическое отрицательных чисел:", averageNegative3);
console.log("Количество нулей:", zeroCount);

