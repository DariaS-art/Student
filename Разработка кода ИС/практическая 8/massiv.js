// Задание 1
var arr1 = []; // Создаем пустой массив для хранения случайных чисел
for (var i = 0; i < 15; i++) {
    // Генерируем 15 случайных чисел в интервале [-10, 30]
    arr1.push(Math.floor(Math.random() * (30 - (-10) + 1)) + (-10));
}
console.log("Массив:", arr1); // Выводим сгенерированный массив

var positiveSum = 0; // Переменная для суммы положительных элементов
var positiveCount = 0; // Переменная для подсчета положительных элементов
for (var j = 0; j < arr1.length; j++) {
    if (arr1[j] > 0) { // Проверяем, является ли элемент положительным
        positiveSum += arr1[j]; // Добавляем положительный элемент к сумме
        positiveCount++; // Увеличиваем счетчик положительных элементов
    }
}
var averagePositive = positiveCount > 0 ? positiveSum / positiveCount : 0; // Вычисляем среднее арифметическое положительных элементов
console.log("Среднее арифметическое положительных элементов:", averagePositive); // Выводим результат

